from astropy.io import fits
from astropy.wcs import WCS

from astropy.nddata import NDData
from specutils.spectra.spectrum1d import SpectrumMixin

hdu = fits.open('l1448_13co_small.fits')[0]
data = hdu.data
wcs = WCS(hdu.header)

class SpectralCube(NDData, SpectrumMixin):
    pass

nd = SpectralCube(data=data, wcs=wcs)

print(nd.dispersion)

